class Solution {
    public int minArray(int[] numbers) {
        //todo
        if (numbers.length == 0)
            return -1;
        if (numbers.length == 1)
            return numbers[0];
        int lo = 0;
        int hi = numbers.length - 1;
        if (numbers[hi] > numbers[lo])
            return numbers[lo];
        while (hi != lo + 1){
            int mid = hi - (hi - lo) / 2;
            if (numbers[mid] < numbers[lo])
                hi = mid;
            else lo = mid;
        }
        return numbers[hi];
    }
}
